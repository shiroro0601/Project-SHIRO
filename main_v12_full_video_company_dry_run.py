from pprint import pprint

from company.artifacts.script_artifact_parser import ScriptArtifactParser
from company.core.employee_role import (
    EditorRole,
    ImageRole,
    PublisherRole,
    ResearchRole,
    ReviewerRole,
    VoiceRole,
    WriterRole,
)
from company.core.task import TaskType
from company.core.task_factory import TaskFactory
from company.publishers.youtube_studio_publisher import YouTubeStudioPublisher


class DryRunTextProvider:
    def generate(self, prompt: str) -> str:
        if "調査してください" in prompt:
            return f"research_result: {prompt}"
        if "台本を書いてください" in prompt:
            return f"script_result: {prompt}"
        if "レビューしてください" in prompt:
            return f"review_result: {prompt}"
        return f"text_result: {prompt}"


class DryRunImageGenerator:
    def generate(self, prompt: str) -> str:
        return "dry_run_image.png"


class DryRunVoiceGenerator:
    def generate(self, text: str) -> str:
        return "dry_run_voice.wav"


class DryRunEditor:
    def generate(self, image_paths: list[str], audio_paths: list[str]) -> str:
        return "dry_run_video.mp4"


class VideoEditorAdapter:
    def __init__(self, editor):
        self.editor = editor

    def generate(self, task):
        input_data = getattr(task, "input_data", {}) or {}
        return self.editor.generate(
            image_paths=input_data.get("image_paths", []),
            audio_paths=input_data.get("audio_paths", []),
        )


def create_task(topic: str, instruction: str, extra_input: dict | None = None):
    input_data = {"theme": topic}
    if extra_input:
        input_data.update(extra_input)

    return TaskFactory.create_task(
        task_type=TaskType.GENERAL,
        instruction=instruction,
        input_data=input_data,
    )


def run_role(role, task, role_name: str, execution_order: list[str]):
    execution_order.append(role_name)
    role.prepare(task)
    result = role.execute(task)
    return role.finalize(result)


def create_publish_metadata(topic: str) -> dict:
    return {
        "title": topic,
        "description": "Generated by Project SHIRO",
        "tags": ["AI", "雑学", "Shorts"],
        "status": "draft",
    }


class FullAutoVideoPipeline:
    def __init__(
        self,
        researcher=None,
        writer=None,
        reviewer=None,
        text_provider=None,
        image_generator=None,
        voice_generator=None,
        editor=None,
        publisher=None,
        script_artifact_parser=None,
    ):
        text_provider = text_provider or DryRunTextProvider()
        self.researcher = researcher or ResearchRole(provider=text_provider)
        self.writer = writer or WriterRole(provider=text_provider)
        self.reviewer = reviewer or ReviewerRole(provider=text_provider)
        self.image_generator = image_generator or DryRunImageGenerator()
        self.voice_generator = voice_generator or DryRunVoiceGenerator()
        self.editor = editor or DryRunEditor()
        self.publisher = publisher or YouTubeStudioPublisher(dry_run=True)
        self.script_artifact_parser = script_artifact_parser or ScriptArtifactParser()

    def run(self, topic: str):
        execution_order: list[str] = []

        execution_order.append("Researcher")
        research_result = self.researcher.execute(topic)

        execution_order.append("Writer")
        script_result = self.writer.execute(research_result)
        script_artifact = self.script_artifact_parser.parse(script_result)

        execution_order.append("Reviewer")
        review_result = self.reviewer.execute(script_result)

        if script_artifact.scenes:
            image_prompt_source = script_artifact.scenes[0].image_prompt
        elif script_artifact.image_prompts:
            image_prompt_source = script_artifact.image_prompts[0]
        else:
            image_prompt_source = script_result
        image_task = create_task(
            topic,
            f"{topic}の画像素材を制作してください。",
            {
                "topic": image_prompt_source,
                "script_result": script_result,
                "script_artifact": script_artifact,
                "review_result": review_result,
            },
        )
        voice_task = create_task(
            topic,
            f"{topic}の音声素材を制作してください。",
            {
                "script_result": script_result,
                "script_artifact": script_artifact,
                "review_result": review_result,
            },
        )
        image_path = run_role(
            ImageRole(generator=self.image_generator),
            image_task,
            "Image",
            execution_order,
        )
        voice_path = run_role(
            VoiceRole(generator=self.voice_generator),
            voice_task,
            "Voice",
            execution_order,
        )

        edit_task = create_task(
            topic,
            f"{topic}の動画を編集してください。",
            {
                "image_paths": [image_path],
                "audio_paths": [voice_path],
            },
        )
        video_path = run_role(
            EditorRole(editor=VideoEditorAdapter(self.editor)),
            edit_task,
            "Editor",
            execution_order,
        )

        publish_task = create_task(
            topic,
            f"{topic}のYouTube投稿を準備してください。",
            {
                "video_path": video_path,
                "metadata": create_publish_metadata(topic),
            },
        )
        publish_result = run_role(
            PublisherRole(publisher=self.publisher),
            publish_task,
            "Publisher",
            execution_order,
        )

        return {
            "topic": topic,
            "execution_order": execution_order,
            "research_result": research_result,
            "script_result": script_result,
            "script_artifact": script_artifact,
            "review_result": review_result,
            "image_path": image_path,
            "voice_path": voice_path,
            "video_path": video_path,
            "publish_result": publish_result,
        }


def run_demo(
    topic: str = "猫の意外な雑学",
    text_provider=None,
    image_generator=None,
    voice_generator=None,
    editor=None,
    publisher=None,
):
    pipeline = FullAutoVideoPipeline(
        text_provider=text_provider,
        image_generator=image_generator,
        voice_generator=voice_generator,
        editor=editor,
        publisher=publisher,
    )
    return pipeline.run(topic)


def main() -> None:
    print("Project SHIRO Version1.2 Full Video Company Dry Run")
    print("Flow: Researcher -> Writer -> Reviewer -> Image -> Voice -> Editor -> Publisher")
    print()
    result = run_demo()
    pprint(result)


if __name__ == "__main__":
    main()
